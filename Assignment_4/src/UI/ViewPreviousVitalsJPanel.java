    private void populatefields() {
        
        if (person != null) {
            ArrayList<VitalSign> vitalSignList = person.getPatient().getVitalSignHistory().getHistory();
            
            if (vitalSignList.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No vital signs found. Please"
                        + " add vital signs", "Error", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            
            for (VitalSign vitalSign : vitalSignList) {
                txt_PatientName.setText(person.getPersonLastName() + ", " + person.getPersonFirstName());
                txt_RespiratoryRate.setText(vitalSign.getRespiratoryRate().toString()); 
                txt_HeartRate.setText(vitalSign.getHeartRate().toString());
                txt_BloodPressure.setText(vitalSign.getBloodPressure().toString());
                txt_Weight.setText(Double.toString(vitalSign.getWeight()));
                txt_Temperature.setText(Double.toString(vitalSign.getTemperature()));
                txt_Status.setText(vitalSign.getStatus());
                txt_DateAndTime.setText(vitalSign.getTimestamp().toString());
            }
        }
    }
